def generate_tick(scenario: str = "normal") -> TelemetryTick:
    """
    Generates one mock telemetry reading.
    scenario controls which kind of situation gets simulated:
    'normal', 'sleeping', 'stressed_commuting', 'emergency_spike'
    """
    now = datetime.utcnow().isoformat()

    if scenario == "sleeping":
        hr = random.randint(48, 60)
        accel = round(random.uniform(0.0, 0.1), 2)
        speed = 0.0
        app = "idle"
    elif scenario == "stressed_commuting":
        hr = random.randint(95, 115)
        accel = round(random.uniform(0.3, 0.8), 2)
        speed = round(random.uniform(3, 15), 1)
        app = "navigation"
    elif scenario == "emergency_spike":
        hr = random.randint(140, 170)
        accel = round(random.uniform(0.0, 0.05), 2)   # HR spikes while sitting still
        speed = 0.0
        app = "idle"
    else:  # normal
        hr = random.randint(65, 85)
        accel = round(random.uniform(0.1, 0.3), 2)
        speed = round(random.uniform(0, 5), 1)
        app = random.choice(["beads_counter", "idle", "messaging"])

    return {
        "timestamp": now,
        "heart_rate": hr,
        "accelerometer": accel,
        "gps_speed": speed,
        "active_app": app,
        "battery": random.randint(15, 100),
    }

def generate_stream(n: int, scenario: str = "normal") -> List[TelemetryTick]:
    """Generates n ticks for a given scenario — useful for quick testing."""
    return [generate_tick(scenario) for _ in range(n)]

