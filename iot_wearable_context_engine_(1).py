from langchain_core.messages import SystemMessage, HumanMessage

PROFILER_SYSTEM_PROMPT = """You are the Profiler Agent for a wearable device.
Given a rolling window of telemetry readings, summarize the user's current
state in ONE short sentence.

Use these reference ranges when describing heart rate:
- Below 60 bpm: low/resting
- 60-100 bpm: normal
- 100-140 bpm: elevated
- Above 140 bpm: dangerously high

Always explicitly state if heart rate is "dangerously high" when any reading
exceeds 140 bpm, especially if accelerometer is near 0 (meaning the user is
not moving). Do not soften or downplay high readings. Do not make medical
diagnoses, but be direct and factual about numbers that fall outside normal
ranges."""

def profiler_node(state: AgentState) -> AgentState:
    """
    Reads the telemetry window from state, asks the LLM for a one-line
    summary, and writes it back into state['profile_summary'].
    This is a LangGraph node: takes AgentState in, returns updated AgentState.
    """
    window = state["telemetry_window"]
    if not window:
        state["profile_summary"] = "No telemetry data available."
        return state

    # Format the window compactly to keep token usage low (Groq TPM limit)
    readings_text = "\n".join(
        f"HR:{t['heart_rate']} Accel:{t['accelerometer']} "
        f"Speed:{t['gps_speed']} App:{t['active_app']} Batt:{t['battery']}%"
        for t in window
    )

    messages = [
        SystemMessage(content=PROFILER_SYSTEM_PROMPT),
        HumanMessage(content=f"Recent readings:\n{readings_text}")
    ]

    response = profiler_llm.invoke(messages)
    state["profile_summary"] = response.content.strip()
    return state

