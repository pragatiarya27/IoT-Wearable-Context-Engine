from typing import TypedDict, List, Dict, Optional
from datetime import datetime

class TelemetryTick(TypedDict):
    timestamp: str
    heart_rate: int          # bpm
    accelerometer: float     # magnitude, roughly 0 (still) to 3+ (running)
    gps_speed: float         # km/h, 0 = stationary
    active_app: str          # e.g. "beads_counter", "navigation", "idle"
    battery: int              # percent

class AgentState(TypedDict):
    telemetry_window: List[TelemetryTick]   # rolling window of recent ticks
    profile_summary: Optional[str]           # Profiler Agent's output
    retrieved_context: Optional[List[str]]   # RAG chunks after rerank
    last_action: Optional[str]               # "silent" | "notify" | "emergency"
    last_reasoning: Optional[str]            # Action Agent's justification

def new_state() -> AgentState:
    """Factory for a fresh, empty AgentState."""
    return {
        "telemetry_window": [],
        "profile_summary": None,
        "retrieved_context": None,
        "last_action": None,
        "last_reasoning": None,
    }

