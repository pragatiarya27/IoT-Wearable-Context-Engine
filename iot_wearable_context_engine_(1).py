from langgraph.graph import StateGraph, END

def build_graph():
    """
    Assembles the two nodes into a linear LangGraph pipeline:
    profiler -> action -> end.
    We keep it linear for now (no branching) since the branching
    logic already lives inside action_node's JSON decision — LangGraph
    here mainly gives us a clean, extensible structure to add more
    nodes to later (e.g. a retriever node, a memory node).
    """
    graph = StateGraph(AgentState)

    graph.add_node("profiler", profiler_node)
    graph.add_node("action", action_node)

    graph.set_entry_point("profiler")
    graph.add_edge("profiler", "action")
    graph.add_edge("action", END)

    return graph.compile()

