ngrok.set_auth_token(NGROK_AUTH_TOKEN)

# 2. CLEANUP (The fix for the connection error)
print("🔄 Cleaning up old tunnels...")
try:
    # Explicitly tell the ngrok cloud to disconnect active tunnels
    tunnels = ngrok.get_tunnels()
    for tunnel in tunnels:
        ngrok.disconnect(tunnel.public_url)

    # Kill the local process
    ngrok.kill()

    # CRITICAL: Give the ngrok cloud backend 5-10 seconds to register the release
    print("⏳ Waiting for ngrok cloud to release your domain...")
    time.sleep(8)
except Exception as e:
    print(f"Cleanup note: {e}")

# 3. Run Streamlit in the background
print("📦 Starting Streamlit...")
streamlit_process = subprocess.Popen(
    ["streamlit", "run", "streamlit_app.py", "--server.port", "8501", "--server.headless", "true"]
)
time.sleep(5)

# 4. Open a public tunnel
try:
    print("🔗 Connecting tunnel...")
    public_url = ngrok.connect(8501)
    print("\n🚀 Streamlit app is live at:", public_url)
except Exception as e:
    print(f"\n❌ Connection failed: {e}")
    print("💡 Quick Fix: Go to https://dashboard.ngrok.com/endpoints and manually clear the active endpoint.")

