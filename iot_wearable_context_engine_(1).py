def chunk_markdown(text: str, min_chunk_len: int = 40) -> list:
    """
    Splits on markdown headers (##) so each chunk is one self-contained
    rule/topic — this is a natural chunk boundary for structured reference
    docs, better than fixed-size splitting which could cut a rule in half.
    """
    sections = text.split("## ")
    chunks = []
    for section in sections:
        section = section.strip()
        if len(section) < min_chunk_len:
            continue
        # re-add the header marker for readability, except for content before first header
        chunk = "## " + section if not section.startswith("#") else section
        chunks.append(chunk)
    return chunks

