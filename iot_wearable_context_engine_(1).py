def retrieve_and_rerank(query: str, top_k_retrieve: int = 5, top_k_final: int = 3) -> list:
    """
    Two-stage retrieval:
    1. Chroma does fast vector search to get top_k_retrieve candidates
    2. Cross-encoder reranks those candidates for precision, keeps top_k_final
    """
    query_embedding = embedder.encode([query]).tolist()
    results = collection.query(
        query_embeddings=query_embedding,
        n_results=top_k_retrieve
    )
    candidates = results["documents"][0]

    if not candidates:
        return []

    # Cross-encoder scores each (query, candidate) pair directly
    pairs = [[query, c] for c in candidates]
    scores = reranker.predict(pairs)

    # Sort candidates by reranker score, descending, keep top_k_final
    ranked = sorted(zip(candidates, scores), key=lambda x: x[1], reverse=True)
    return [doc for doc, score in ranked[:top_k_final]]

