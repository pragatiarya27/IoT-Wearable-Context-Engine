import time

def run_evaluation(scenarios: list, num_ticks: int = 4, delay_seconds: float = 2.0) -> list:
    """
    Runs the compiled graph once per test scenario, records the actual
    action taken, and compares it against the expected_action label.
    """
    results = []

    for case in scenarios:
        state = new_state()
        state["telemetry_window"] = generate_stream(num_ticks, scenario=case["scenario"])
        state = app_graph.invoke(state)

        actual = state["last_action"]
        correct = (actual == case["expected_action"])

        results.append({
            "id": case["id"],
            "scenario": case["scenario"],
            "expected": case["expected_action"],
            "actual": actual,
            "correct": correct,
            "reasoning": state["last_reasoning"]
        })

        print(f"Case {case['id']} ({case['scenario']}): "
              f"expected={case['expected_action']}, actual={actual}, "
              f"{'✅' if correct else '❌'}")

        time.sleep(delay_seconds)  # stay under Groq's rate limit

    return results

eval_results = run_evaluation(test_scenarios)

def score_results(results: list):
    total = len(results)
    correct = sum(1 for r in results if r["correct"])
    accuracy = correct / total if total > 0 else 0

    # Emergency-specific recall — the metric that matters most for safety
    emergency_cases = [r for r in results if r["expected"] == "emergency"]
    emergency_caught = sum(1 for r in emergency_cases if r["actual"] == "emergency")
    emergency_recall = emergency_caught / len(emergency_cases) if emergency_cases else None

    # False positives — non-emergency cases that got escalated anyway
    false_emergencies = [r for r in results if r["expected"] != "emergency" and r["actual"] == "emergency"]

    print(f"Overall accuracy: {correct}/{total} ({accuracy:.0%})")
    if emergency_recall is not None:
        print(f"Emergency recall: {emergency_caught}/{len(emergency_cases)} ({emergency_recall:.0%})")
    print(f"False emergency triggers: {len(false_emergencies)}")

    if false_emergencies:
        print("\n⚠️ False emergency cases (review these):")
        for r in false_emergencies:
            print(f"  Case {r['id']} ({r['scenario']}): {r['reasoning']}")

